def get_plus(a, b):
    return a+b

def get_minus(a, b):
    return a-b

def get_multi(a, b):
    return a*b

def get_div(a, b):
    return a/b

def get_mod(a, b):
    return a%b


if __name__ == '__main__':
    print(get_plus(1, 1))
    print(get_minus(3, 2))
    print(get_multi(4, 4))
    print(get_div(8, 4))
    print(get_mod(10, 3))